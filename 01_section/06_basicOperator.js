// Basic Operator

let birthYear = 2002;
let currentYear = 2025;
let age = currentYear - birthYear;
console.log(age); // minus operator

console.log(age * 3, age / 2, 5 ** 2); // we can also print multiple things
// Note: 5 ** 2 (5 power of 2, the second value is power)

// Assignment Operators
let a = 2 + 5; // equal to '=' sign is called assignment operator

a += 10; // a = a + 10 (compact way to right)
a *= 5;

// Unary Operator(it needs only one operand)
a++;
a--;
console.log(a);

// Comaprison Operator
let myAge = 23;
let myBrotherAge = 17
console.log(myAge > myBrotherAge); // <, >, <=, >=

console.log(myAge >= 18);
console.log(myBrotherAge >= 18);