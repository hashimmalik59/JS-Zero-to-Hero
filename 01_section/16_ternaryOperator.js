// Ternary Operator
// For checking only 2 conditions we use "Ternary Operator"

const myAge = 19;
//         if                      else
myAge > 17 ? console.log("Adult!") : console.log("Not Adult!");

console.log(`Now i'm ${myAge > 17 ? "Adult" : "Not Adult"}`); // we can also use it in template literals